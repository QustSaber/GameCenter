package com.anjoyo.gamecenter.constant;

public class GameInfo {

	private int id;
	private int star;
	private int price;
	private String title;
	private String icon;
	private String version;
	private String filesize;
	private int onclick;
	private int infopfen;
	private int infopfennum;
	private String flashurl;
	private String pachagename;
	private String titlepic;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getStar() {
		return star;
	}

	public void setStar(int star) {
		this.star = star;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getIcon() {
		return icon;
	}

	public void setIcon(String icon) {
		this.icon = icon;
	}

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public String getFilesize() {
		return filesize;
	}

	public void setFilesize(String filesize) {
		this.filesize = filesize;
	}

	public int getOnclick() {
		return onclick;
	}

	public void setOnclick(int onclick) {
		this.onclick = onclick;
	}

	public int getInfopfen() {
		return infopfen;
	}

	public void setInfopfen(int infopfen) {
		this.infopfen = infopfen;
	}

	public int getInfopfennum() {
		return infopfennum;
	}

	public void setInfopfennum(int infopfennum) {
		this.infopfennum = infopfennum;
	}

	public String getFlashurl() {
		return flashurl;
	}

	public void setFlashurl(String flashurl) {
		this.flashurl = flashurl;
	}

	public String getPachagename() {
		return pachagename;
	}

	public void setPachagename(String pachagename) {
		this.pachagename = pachagename;
	}

	public String getTitlepic() {
		return titlepic;
	}

	public void setTitlepic(String titlepic) {
		this.titlepic = titlepic;
	}

}
